            <section class="sec05">
                <h2 class="border-red">린나이 설립 배경</h2>
                <div class="inner">
                    <p class="lead">린나이는 창립 정신을 바탕으로, 검증된 기술로 ‘열과 일상’ 더 나은 방향으로 만들어갑니다</p>
                    <div class="l-row mat20">
                        <div class="l-col-5 l-col-sm-12">
                            <p class="cont-text">
                                1918년 11월의 어느 날, <b>나이토 히데지로</b>는 한 가게 앞에서 일본 전통 간식인 이마가와야키(주로 팥소를 넣어 굽는 빵)가 만들어지는 모습을 넋을 잃고 바라보고 있었습니다.
                                그의 시선을 사로잡은 것은 이마가와야키를 굽는 데 사용되던 <b>수입 유류 연소식 조리용 스토브</b>에서 피어오르던 푸른 불꽃이었습니다.
                                비슷한 스토브를 직접 만들어보고 싶었던 히데지로는 가게 주인을 설득해 그 스토브를 넘겨받았고, 이를 모델로 <b>석유 연료 스토브</b>를 개발했습니다. 이것이 오늘날 우리가 아는 사업의 시작이 되었습니다.
                            </p>
                            <p class="cont-text">
                                그로부터 2년 뒤인 1920년, 히데지로는 나고야가스(현 도호가스株式会社)를 퇴직했습니다. 그리고 같은 기숙사에서 지내던 소꿉친구 <b>하야시 가네키치</b>와 함께 오늘날 린나이로 이어지는 전신인 ‘린나이 상회(Rinnai & Co.)’를 설립했습니다.
                                ‘린나이’라는 사명은 두 사람의 성에서 한 글자씩 따온 것으로, <b>‘린(Rin)’은 ‘하야시(Hayashi)’의 다른 음독에서, ‘나이(Nai)’는 ‘나이토(Naito)’에서</b> 가져왔습니다.
                                이후 린나이는 약 100년에 걸쳐 최신 열에너지 기기를 개발해 왔습니다.
                                일본이 근대화를 이어가는 동안 린나이의 제품은 시간의 흐름 속에서 일본의 생활 방식을 조금씩 변화시켜 왔습니다. 창업 초기부터 오늘에 이르기까지 린나이는 “열을 통해 사회에 편안한 생활을 제공한다”는 기업 미션을 중심에 두고 나아가고 있습니다.
                                초기 석유 연료 스토브의 카탈로그에서도 확인할 수 있는 <b>‘사회에 기여하겠다’</b>는 정신은 지금까지도 변함없이 이어지고 있습니다.
                            </p>

