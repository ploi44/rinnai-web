린나이코리아㈜는 귀하의 개인정보보호를 매우 중요시하며,『정보통신망이용촉진등에관한법률』상의 개인정보보호 규정 및 정보통신부가 제정한 『개인정보보호지침』을 준수하고 있습니다.
