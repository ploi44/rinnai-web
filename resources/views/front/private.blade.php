- 선택 항목 : 자택 주소, 실제 생년월일, 직업, 결혼 여부 등.(단, 출장 및 상담 서비스신청 시에 주소는 필수입력사항입니다.)
② 수집 방법: 홈페이지를 통한 회원 정보 수집, 로그분석 프로그램을 통한 자동 로그 수집
