* 성명, 아이디, 비밀번호, 이메일 주소, 전화번호 : 회원제 서비스 이용에 따른 본인 식별 절차에 이용.
* 이메일 주소, 전화번호: 고지사항 전달, 불만처리 등을 위한 원활한 의사소통 경로의 확보, 새로운 서비스 및 뉴스, 이벤트 정보 등의 안내.
